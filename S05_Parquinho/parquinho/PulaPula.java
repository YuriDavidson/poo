package parquinho;

public class PulaPula {

    private int capacidade = 4;
    private float saldo ;

    public PulaPula() {
        
        this.saldo = 0;
        
    }

    public int getCapacidade() {
        return capacidade;
    }

    public void setCapacidade(int capacidade) {
        this.capacidade = capacidade;
    }

    public float getSaldo() {
        return saldo;
    }

    public void setSaldo(float saldo) {
        this.saldo = saldo;
    }
    
    
    
}
